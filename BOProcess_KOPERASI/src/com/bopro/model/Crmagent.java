package com.bopro.model;

/**
 *
 * @author syukur
 */
public class Crmagent {
    private String codecrm;
    private String agent_id;
    private String agentname;
    private String posisi;
    private String nama;
    private String numberphone;
    private String email;

    /**
     * @return the codecrm
     */
    public String getCodecrm() {
        return codecrm;
    }

    /**
     * @param codecrm the codecrm to set
     */
    public void setCodecrm(String codecrm) {
        this.codecrm = codecrm;
    }

    /**
     * @return the posisi
     */
    public String getPosisi() {
        return posisi;
    }

    /**
     * @param posisi the posisi to set
     */
    public void setPosisi(String posisi) {
        this.posisi = posisi;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the numberphone
     */
    public String getNumberphone() {
        return numberphone;
    }

    /**
     * @param numberphone the numberphone to set
     */
    public void setNumberphone(String numberphone) {
        this.numberphone = numberphone;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the agent_id
     */
    public String getAgent_id() {
        return agent_id;
    }

    /**
     * @param agent_id the agent_id to set
     */
    public void setAgent_id(String agent_id) {
        this.agent_id = agent_id;
    }

    /**
     * @return the agentname
     */
    public String getAgentname() {
        return agentname;
    }

    /**
     * @param agentname the agentname to set
     */
    public void setAgentname(String agentname) {
        this.agentname = agentname;
    }
}
